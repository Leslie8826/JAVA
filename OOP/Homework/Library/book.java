public class book {
	String Title;
	String Author;
	String Editor;
	
	public void setTitle(String s){
		Title = s;
	}
	
	public void setAuthor(String s){
		Author = s;
	}
	
	public void setEditor(String s){
		Editor = s;
	}
	
	public String getTitle() {
		return Title;
	}
	
	public String getAuthor() {
		return Author;
	}
	
	public String getEditor() {
		return Editor;
	}
}

